#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*tmp;

	tmp = s1;
	if (n == 0)
		return (s1);
	while (*s1 != '\0')
		s1++;
	while (n && *s2 != '\0')
	{
		*s1++ = *s2++;
		n--;
	}
	*s1 = '\0';
	return (tmp);
}
